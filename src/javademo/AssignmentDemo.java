package javademo;

public class AssignmentDemo {
  public static void main(String[] args) {
	  
	  int x = 8;
	  int y = 6;
	  
	  x+=y; // x =x+y = 11
	  System.out.println("x = " +x);
	  
	  x-=y; // x =x-y = 11
	  System.out.println("x = " +x);
	  x*=y; // x =x*y = 48
	  System.out.println("x = " +x);
	  x/=y; // x =x+y = 8
	  System.out.println("x = " +x);
	  x%=y; // x =x+y = 2
	  System.out.println("x = " +x);

  }
  
}
